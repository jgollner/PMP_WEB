<script type="text/javascript">
$(document).ready(function(){
$('.carousel').on('afterChange', function(event, slick, currentSlide){
	//console.log('carousel changed to: ' + currentSlide + ' ' + $('#projectlist').val());
	//get the index of the current slide and set the drop down list to that item, too
	$('#projectlist').prop("selectedIndex",currentSlide).selectmenu("refresh");
});
$("#projectlist").selectmenu({
	change: function( event, data ) {
		//console.log('list changed to: '+data.item.index);
		//get the index of the current drop down and set the carousel to that item, too
		$('.carousel').slick('slickGoTo', data.item.index);
	}
}).selectmenu( "menuWidget" ).addClass( "listoverflow" );

$('.carousel').slick({
dots: true,
infinite: true,
speed: 500,
slidesToShow: 1,
slidesToScroll: 1
});
});
</script>
